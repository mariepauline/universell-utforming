  <h1>Hvorfor?</h1>
  <h2>Hvorfor er universell utforming viktig?</h2>

  <p>
    Universell utforming bygger på tanken om at tjenester skal være
    tilgjengelige for alle, uavhengig av alder, funksjonsevne og utdanningsnivå.
    Ved å gi for mangfoldet i befolkningen gir man den enkelte rom for å utfolde
    seg, bestemme selv og delta på like vilkår.
  </p>

  <p>
    Se for deg at du skal kjøpe en brus i en brusautomat. Brusautomaten er
    ganske standard oppbygd, med bilde av en stor Colaflaske på forsiden, et
    hull der brusen kan komme ut nede og et panel med knapper på høyre side. Du
    har skikkelig lyst på en flaske med Solo og skal finne riktig knapp for
    dette, men istedenfor bilde av brusen eller navnet på brusen skrevet med
    bokstaver så står det ingenting over knappene. Det er tilsynelatende
    ingenting som skiller knappene hvis man bare bruker øynene til å se på dem.
  </p>
  <p>
    Etter litt nøyere undersøkelse kjenner du at det kommer litt ulik lukt fra
    de forskjellige knappene. Du kjenner ikke igjen noen av luktene, og klarer
    hvertfall ikke å knytte luktene til brus. Forskjellen mellom knappene er
    også så liten at du såvidt klarer å kjenne at lukten er ulik.
  </p>
  <p>
    Det viser seg at de som har laget brusautomaten, CanineSoda &trade;, har
    ekstremt god luktesans og har lært seg å knytte ulike lukter til ulik
    mening. De har utviklet et eget språk ved bruk av lukter som du aldri har
    hørt om før. Denne superluktesansen har derimot ikke du, og det finnes ingen
    måte å utvikle denne ekstra sansen.
  </p>
  <p>
    Å få tak i rett brus fra denne automaten høres ganske vanskelig ut, ikke
    sant? Problemet koker ned til at brusautomaten antar at du som bruker både
    skal ha overmenneskelig luktesans i tillegg til å kunne dette spesielle
    luktespråket.
  </p>
  <p>
    Se nå for deg at du er blind og skal kjøpe billett til bussen som tar deg
    hjemmefra og ned til sentrum. Billettautomaten har ingen knapper, kun en
    stor touchskjerm. Du klarer ikke å se hva som står på skjermen, du klarer
    ikke finne noen knapp for leser opp innholdet for deg og dette er eneste
    måte å kjøpe billett. Utviklerne av denne automaten har tydelig glemt at
    blinde og svaksynte også kan ønske å ta bussen og ikke tilrettelagt for dem.
  </p>
  <p>
    Ved å ikke tilrettelegge for alle brukere vil man kunne ende opp med et
    produkt eller en tjeneste som du i praksis nekter en brukergruppe å bruke.
    Om svaksynte ikke får kjøpt bussbillett på grunn av billettautomaten nekter
    du dem å ta bussen og begrenser deres mulighet til å ferdes trygt rundt om i
    byen. Dette kan igjen føre til at de ikke kommer seg til legen, til
    matbutikken og for å besøke familie.
  </p>
  <p>
    Dette er bare et eksempel på en situasjon der alle brukere ikke har like
    muligheter, og det finnes utallige flere. Universell utforming handler altså
    om likeverdig deltagelse i samfunnet.
  </p>

  <h2>Hvem er brukerne?</h2>
  <p>
    Ifølge Statistisk Sentralbyrå
    <a
      href="https://www.ssb.no/arbeid-og-lonn/sysselsetting/statistikk/personer-med-nedsatt-funksjonsevne-arbeidskraftundersokelsen"
      target="_blank"
      rel="noopener noreferrer"
    >
      har 1 av 5 nedsatt funksjonsevne
    </a>. Derfor er det viktig å kjenne brukerne sine og hvilke behov de kan ha.
    Her er noen eksempler på utfordringer som brukere kan ha:
  </p>
  <h3>Motorikk</h3>
  <p>
    Motoriske funksjonsnedsettelser viser til alt som har med motorikk og
    bevegelse å gjøre. Personer i disse målgruppene trenger ofte andre måter å
    styre og navigere digitalt utstyr enn med vanlig tastatur og mus. Det kan
    være skjermtastatur, joystick, knappestyring, øyestyring eller spesialbygde
    løsninger. Som et resultat vil mange ha redusert presisjon og ofte trenge
    lengre tid til å fullføre oppgaver.
  </p>
  <p>
    Mange av disse brukerne har også begrenset mulighet til å forflytte seg fra
    et sted til et annet, ofte på grunn av manglende tilgjengelighet i det
    fysiske miljøet. Internett kan da være en viktig kanal for å kunne delta i
    samfunnslivet, og for eksempel å kunne planlegge reiser og aktiviteter i
    forkant.
  </p>
  <h3>Syn</h3>
  <p>
    Synshemming er en fellesbetegnelse for både blindhet og svaksynthet.
    Personer med nedsatt syn kan ha problemer med liten tekst, små klikkeflater,
    uvanlige skrifttyper, svak kontrast og enkelte fargekombinasjoner mellom
    tekst og bakgrunn. Brukere som ser dårlig kan også ha vanskelig for å få
    overblikk, overser ofte objekt som ikke benyttes konsekvent og det kan ta
    litt lengre tid å finne frem.
  </p>
  <p>
    Personer som er blinde behøver et hjelpemiddel for å kunne navigere og
    forstå innholdet på nettstedet. Hjelpemiddelet, også kjent som skjermleser,
    er avhengig av god kode for å kunne konvertere til tale og punktskrift.
  </p>
  <p>
    Personer med fargeblindhet kan ha problemer å oppfatte meningsbærende
    fargeforskjeller og skille mellom farger som presenteres sammen, for
    eksempel i et diagram.
  </p>
  <h3>Hørsel</h3>
  <p>
    Personer med hørselshemming kan ha problemer med å høre innholdet i en film
    eller lydfil og behøver å få innholdet presentert i tekst. Nedsatt hørsel
    kan være medfødt og skyldes arv, komplikasjoner i svangerskapet eller under
    fødselen. Andre får nedsatt hørsel senere i livet. Årsaken kan være påførte
    skader og sykdommer, som for eksempel kraftig støy eller
    hjernehinnebetennelse.
  </p>
  <h3>Kognisjon</h3>
  <p>
    Personer med kognitive funksjonsnedsettelser kan ha problemer med å forstå
    bare tekst. Derfor er det viktig å kombinere tekst med bilder,
    illustrasjoner, film og lyd. Jo flere ulike måter du informerer på, kan
    desto flere ta til seg innholdet. Når det gjelder selve teksten er det
    alltid viktig å skrive enkelt og forståelig, å forklare vanskelige ord og å
    formatere teksten godt. Formulering av tekst og presentasjon av innhold er
    særlig viktig for disse målgruppene.
  </p>
  <p>For mange med kognitive utfordringer er det derfor viktig at</p>
  <ul>
    <li>
      informasjonsstruktur og navigering er logisk, intuitiv og konsekvent
    </li>
    <li>
      objekter utformes slik at brukeren forstår hvilke som er viktige og i
      hvilken rekkefølge de skal benyttes
    </li>
    <li>
      brukeren får støtte til å gjøre riktig med hjelp av tydelige instruksjoner
    </li>
    <li>
      brukeren får hjelp av forståelige feilmeldinger dersom noe blir feil
    </li>
    <li>lang eller komplisert informasjon deles opp i kortere deler</li>
    <li>
      steg i en prosess nummereres slik at brukeren vet hvor hun befinner seg
    </li>
  </ul>
  <p>
    Jo tydeligere grensesnittet er, desto lettere er det for brukerne å finne
    fram og ta til seg informasjonen.
  </p>

