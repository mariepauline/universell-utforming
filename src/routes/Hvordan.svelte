  <h2>WCAG 2.0</h2>
  <p>
    <strong>Web Content Accessibility Guidelines (WCAG) 2.0</strong> gir
    retningslinjer for gjengelig webinnhold, og forskrift for universell
    utforming stiller krav om at nettsider må oppfylle 35 av 61 suksesskriterier
    i WCAG 2.0. Dette er derfor en god liste å ta utgangspunkt i når man skal
    utvikle tilgjengelige nettsider. En god oversikt over minimumskravene i
    standarden finnes på
    <a
      href="https://www.uutilsynet.no/wcag-standarden/wcag-20-standarden/86"
      target="_blank"
      rel="noopener noreferrer"
    >
      uutilsynets nettsider.</a
    > Her finnes det både beskrivelse av kravene og løsningsforslag.
  </p>

  <h2>Testing</h2>
  <p>
    Ved utvikling av et produkt eller en tjeneste er det aller viktigste man kan
    gjøre å kjenne brukerne sine. Dette gjelder uansett, uavhengig av om man
    jobber med universell utforming eller ikke. Brukertesting er derfor en
    viktig del av mange utviklingsløp og er en mulighet for å tilpasse produktet
    eller tjenesten av tilbyr til brukerne slik at de får den beste mulige
    opplevelsen. Uutilsynet har en egen seksjon om
    <a
      href="https://www.uutilsynet.no/regelverk/brukertesting-med-funksjonshemmede/193"
      target="_blank"
      rel="noopener noreferrer"
      >brukertesting med funksjonshemmede
    </a> som gir gode tips rundt temaet.
  </p>
  <p>
    Kravene i WCAG 2.0 kan testes enkelt selv uten brukertesting. Det finnes
    mange gode verktøy som kan analysere nettsiden din og gi vurdering basert på
    WCAG 2.0. Den vil typisk gå gjennom strukturen på siden, farger, alternative
    tekster på bilder og en rekke annet. Om man bruker WCAG 2.0 som utgangspunkt
    og bruker et slikt verktøy for å finne svakheter og retter disse så er man
    langt på vei mot en nettside som er universelt utformet og tilgjengelig for
    alle.
  </p>
  <p>
    Det finnes nettsider som analyserer hele siden din ved at du gir URL-en, men
    det finnes også mange enkle verktøy som kan kjøre underveis i
